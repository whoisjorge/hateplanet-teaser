</template>



<script>
import Preloader from '@/components/Preloader'

export default {
  name: 'App',

  data () {
    return {
      preloading: true
    }
  },

  components: {
    Preloader
  },

  methods: {
    fadePreloader () {
      const app = this
      setTimeout(() => {
        app.preloading = false
      }, 2500)
    },
    boot () {
      this.fadePreloader()
    }
  },

  mounted () {
    this.boot()
  }
}
</script>



